    private void subirfoto2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subirfoto2ActionPerformed

        JFileChooser fileChooser = new JFileChooser();

        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            try {

                Image image = ImageIO.read(selectedFile);

                Image scaledImage = image.getScaledInstance(Foto.getWidth(), Foto.getHeight(), Image.SCALE_DEFAULT);

                Foto.setIcon(new ImageIcon(scaledImage));
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_subirfoto2ActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        try {
        // Verificar si el campo de hora de salida está vacío
        if (horadesalida.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese la hora de salida.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Salir del método si el campo está vacío
        }
        
        // Verificar si el formato de la hora de salida es válido
        String horaSalidaText = horadesalida.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("HH:mm:ss"); // Definir el formato de fecha
        dateFormat.setLenient(false); // No permitir fechas inválidas
        
        try {
            dateFormat.parse(horaSalidaText); // Intentar analizar la cadena como una fecha
        } catch (ParseException e) {
            // Si ocurre una excepción, significa que el formato no es válido
            JOptionPane.showMessageDialog(this, "Formato de hora de salida inválido. Por favor, utilice el formato HH:mm:ss.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Salir del método si el formato es inválido
        }
        
        // Continuar con el proceso de inserción si el campo y el formato son válidos
        String query = "INSERT INTO conductores (nombre_apellidos, numero_conductor, nombre_empresa, edad, identificacion, bus, ruta, hora_salida, hora_entrada, recorrido) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";           
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, nombreyapellidos.getText());
        pst.setString(2, numeroconductor.getText());
        pst.setString(3, nombredeempresa.getText());
        pst.setString(4, edad.getText());
        pst.setString(5, identificacion.getText());
        pst.setString(6, bus.getText());
        pst.setString(7, ruta.getText());
        pst.setString(8, horadesalida.getText());
        pst.setString(9, horadesentrada.getText());
        pst.setString(10, recorrido.getText());
        pst.executeUpdate();
        
        // Mostrar un mensaje de éxito o realizar alguna otra acción después de guardar los datos
        System.out.println("Datos guardados exitosamente");
    } catch (Exception e) {
        System.err.println("Error al guardar los datos: " + e);
    }
    }//GEN-LAST:event_guardarActionPerformed

